                <p>My first memories of life, of my consciousness, were spent living in Smithers B.C. These memories are so distant yet never forgotten; overlooking the Bulkley River flowing below as it twisted its way, etching the lands with its mighty flow.I have many memories of that area, but it was not to be forever.  At an early age we moved to Campbell River B.C. and again the flow of water. The Discovery Passage was ever present in my life. </p>

                <p>It was here in Campbell River that I started early with my interest in art always drawing monsters of one sort or another, having a curious interest in the Munsters and the Addams Family and such other interest as Rat Fink and  Hot Wheels and my Fright Factory. But the 60'S brought with it more, it brought Flower Power, Love, Peace and the Hippy Generation. This all melted into some form of primordial goop of psychedelic colors which I was attracted to.</p>

                <p> I will never forget my own little cartoon monster that I always drew and continued to try and perfect as a child and I remember that my friends often wanted me to draw something or another for them.  My fascination for colour was always there and in some ways was always looking to be expressed though suppressed as I was young and did not understand. I continued drawing with pencils and learning to shade the same old monsters or mythical creatures as reality seems dull in an artistic way.</p>

                <p>The sublime and the surreal needed an outlet but my art still was not mature enough to be expressed. Throughout high school I continued taking art classes and continued producing art and getting A's for my efforts.  During a teacher parent conference meeting in my final year I remember that my parents were told by my art teacher that my talents would not lead to financial success, and like a sail boat can loose wind my dreams of being an artist lost its purpose. I continued producing art, although my belief that I could become something or somebody in the art world was now behind me and I took a job in the grocery business.</p>

                <p>The biggest thing was that I needed artistic expression and continued with taking classes in figure drawing and stained glass, always looking for some medium that would allow my inner expression to be unleashed.  After many different attempts at making extra money I met an artist named Patrick Logan who lived in Campbell River. His expression of art was something that I truly fell in love with. </p>

                <p>I remember saying to Patrick that if I was ever to paint that I would choose to block my colors as he did.  Stained glass was my art of choice and every color is blocked by lead.  It was during this period that I learned about  myself as one of my big interests became philosophy and the  study of my own human nature. </p><blockquote>Know thyself</blockquote><p> "Know thyself" comes to mind and coupled with an ever widening regard of  the great outdoors I became an outdoors enthusiast always looking to reach further out into the land  and yet trying to always further reach into myself.</p>

                <p>In the midst of life, life as I knew it changed and I found myself living In Victoria B.C. After a few attempts at doing things to try and make extra money I was suggested by my girl friend that I should try and sell my art. If all of my books on philosophy had taught me anything it was to follow my heart  and I took her advice. The thing was that I felt I had no style to call my own. So I decided that I would search within myself for what I  considered needed  to be expressed as my art style.</p>

                <p> It was when on my Mothers suggestion that my daughter and I  create banners for  the Campbell River banner project that I finally found myself and started to paint.  All the steps of a lifetime finally became realized in a melting pot of understanding that I had finally found my calling.  I had found ways to limitlessly express myself in a way that was all unto myself.</p>

                <p>So where do things go from here I do not know, I live for the moment.  What I do know is that if you put your mind to something that you love, something will come back to you ten fold in return and that the energy you put forth into something you love is an energy that feels as if there was no effort in creating it.</p>
